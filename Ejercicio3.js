// Operadores de asignación y comparación
const numero1 = 15
const numero2 = 20
const numero3 = '25'

console.log('¿El numero1 es mayor o igual a numero2? :', numero1 >= numero2)
console.log('¿El numero1 es menor o igual a numero2? :', numero1 <= numero2)
console.log('¿El numero1 es menor que numero3? :', numero1 < numero3)
console.log('¿El numero3 es menor que numero2? :', numero3 < numero2)
